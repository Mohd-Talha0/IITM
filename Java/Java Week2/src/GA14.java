public class GA14 {
    public static void main(String args[]) {
        switch (2) { //works same as x = 2, followed by switch(x)
            case 1:
                System.out.println("One");
            case 2:
                System.out.println("Two");
            case 3:
                System.out.println("Three");
            default:
                System.out.println("Default");
                break;
        }
    }
}